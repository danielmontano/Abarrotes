<font face="Comic Sans MS" size="4"><BR>
La cadena fue fundada en la ciudad de Hermosillo en 1977, de acuerdo con un plan de la compañía para promover sus marcas de Cervecería Cuauhtémoc, por lo que al principio sus tiendas sólo vendían cerveza, botanas y cigarros. El nombre de OXTRO viene del símbolo de % que era como se anunciaba y de ahí, gracias a la gente, nació el nombre de OXTRO. En la actualidad esta cadena y su nombre son ampliamente conocidos a lo largo de todo México.
<BR><BR>
En 1978 abrieron las primeras tiendas en la ciudad de Hermosillo, iniciando operaciones al año siguiente en Chihuahua, Monterrey y Mexicali. En 1982 se crea el concepto del líder de tienda, un comisionista mercantil que trabajaba en compañía de su familia atendiendo todos un mismo OXTRO. Hasta 1994, Oxtro era un área de Cervecería Cuauhtémoc Moctezuma, pero en dicho año se convierte en una empresa independiente dentro del grupo de empresas de FEMSA.
<BR><BR>
En México es la tercera cadena con más ventas, después de Soriana. En América Latina, Oxtro es la séptima cadena comercial más grande por ventas.
